#include "../includes/main.h"

int	ft_parse_args(char *str)
{
	int	i;

	if (str[0] == '-' && (!(str[1] >= '0' && str[1] <= '9')))
		return (1);
	i = 0;
	while (str[++i])
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
			return (1);
	}
	return (0);
}
